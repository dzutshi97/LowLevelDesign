package com.lld.MessageQueue.Models;

public class Subscriber {

    int id;
    Topic topic;

    public Subscriber(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Topic getTopic() {
        return topic;
    }

    public void setTopic(Topic topic) {
        this.topic = topic;
    }
}
